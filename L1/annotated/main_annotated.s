	cmpl	$1, %ecx                # Сравниваем n (в ecx) с 1
	jle	.L4                     # Если n <= 1, прыгаем на return 1
	leal	-2(%rcx), %ecx         # ecx = n - 2 (хитрая оптимизация)
	addq	$3, %rcx                # rc = n + 1 (подготовка счетчика)
	movl	$2, %edx                # edx = 2 (счетчик i)
	movl	$1, %eax                # eax = 1 (result)

.L3:                            # ЦИКЛ:
	imulq	%rdx, %rax             # rax = result * i
	addq	$1, %rdx                # i++
	cmpq	%rcx, %rdx             # Сравниваем i с n+1
	jne	.L3                     # Если не равны, продолжаем
	ret                          # Возврат (result в rax)

.L4:
	movl	$1, %eax                # return 1
	ret

# УЛУЧШЕНИЯ -O1:
# 1. Переменные живут в регистрах (%eax, %edx, %ecx)
# 2. Нет пролога/эпилога (функция "плоская")
# 3. Меньше инструкций
# 4. Убраны лишние перемещения в память

