factorial_iter:
	pushq	%rbp                    # Сохраняем base pointer (пролог функции)
	movq	%rsp, %rbp              # Устанавливаем новый stack frame
	subq	$16, %rsp               # Выделяем 16 байт на стеке для локальных переменных
	movl	%ecx, 16(%rbp)          # Сохраняем параметр n (первый аргумент в ecx)
	movq	$1, -8(%rbp)            # result = 1 (локальная переменная)
	movl	$2, -12(%rbp)           # i = 2 (счетчик цикла)
	jmp	.L2                     # Безусловный переход на проверку условия
	
.L3:                            # ТЕЛО ЦИКЛА:
	movl	-12(%rbp), %eax        # Загружаем i в eax
	cltq                         # Расширяем 32-bit до 64-bit (i -> 64-bit)
	movq	-8(%rbp), %rdx         # Загружаем result в rdx
	imulq	%rdx, %rax             # Умножение: rax = result * i
	movq	%rax, -8(%rbp)         # Сохраняем результат обратно в result
	addl	$1, -12(%rbp)          # i++ (инкремент)
	
.L2:                            # ПРОВЕРКА УСЛОВИЯ ЦИКЛА:
	movl	-12(%rbp), %eax        # Загружаем i
	cmpl	16(%rbp), %eax         # Сравниваем i и n
	jle	.L3                     # Если i <= n, прыгаем в тело цикла
	
	movq	-8(%rbp), %rax         # Загружаем result для возврата
	addq	$16, %rsp              # Очищаем стек (эпилог функции)
	popq	%rbp                   # Восстанавливаем base pointer
	ret                          # Возврат из функции

# ОСОБЕННОСТИ -O0:
# 1. Много операций сохранения/загрузки из памяти
# 2. Явные пролог и эпилог функции
# 3. Все переменные хранятся на стеке
# 4. Легко проследить логику программы


